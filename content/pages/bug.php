	<h1 class='title'>Report a Bug</h1>
    <h2>How to report a bug</h2>
    <div id="bugContent">
        <h3>In Game</h2>
        <span>To report a bug while in game, open your console and type 'reportbug {description of bug}'</span>
        <span>Please include:</span>
        <ol>
            <li>What the bug does</li>  
            <li>How to get the bug to work</li>  
        </ol>

        <span>Please also send me a message to let me know there is a bug!</span>

        <h3>Out of Game</h2>
        <span>While out of game, please open an issue at <a href="https://github.com/xxauroraxx/dnd5e/issues">Github</a>.<br><br>A good bug report will include the following:</span>
        <ol>
            <li>A good summary / title. Think of this as a headline that can be used to refer to this specific bug.</li>
            <li>Background information.</li>
            <li>Steps to reproduce & 'why this is a problem'</li>
            <li>A couple of examples.</li>
            <li>Repeatability.</li>
            <li>Additional resources.</li>
        </ol>
    </div>


</div>