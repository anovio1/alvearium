    <h1 class='title'>D&D 5e CSGO</h1>
                    <span class=''>
                        In D&D, players choose a combination of a Race and a Class. New players start as the default Race "Human" and the default Class "Fighter." A Race is a species, like Human or Orc. A Class is a profession, such as Sorcerer or Fighter. You may be any combination of Race and any Class.
        <br>    <br>
        Killing enemies, rescuing hostages, planting the bomb, and defusing the bomb results in the player obtaining experience for their current Class. Players start out as Level 1 for every class. To Level Up a Class the player must earn enough experience. To view how much experience is needed type menu to access the in-game menu. It takes 1000 experience to go from Level 1 to Level 2, 2000 to go from Level 2 to Level 3, and so on.
        <br>    <br>
        Certain classes can only use certain weapons. For instance, a Wizard can not use an AK or AWP. The class with the least restrictions on it is the Fighter, and Sorcerer is the most.
